public class Torba2 {
    String znamka;
    String barva;
    double teza;

    public Torba2(String znamka,String barva,double teza){
        this.znamka=znamka;
        this.barva=barva;
        this.teza=teza;
    }

    public String getZnamka(){
        return znamka;
    }
    public String getBarva(){
        return barva;
    }
    public double getTeza(){
        return teza;
    }

}
